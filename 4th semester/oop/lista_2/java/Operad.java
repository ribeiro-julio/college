public class Operad {
    public static void main(String args[]) {
        System.out.println("Impressão de Resultado de Cálculos:");
        
        int a = 3, b = 2;
        
        int c = a+b;
        System.out.println("O resultado da soma de A e B é:" + c);
        c = a-b;
        System.out.println("O resultado da subtracao de A e B é:" + c);
        c = a*b;
        System.out.println("O resultado da multiplicacao de A e B é:" + c);
        c = a/b;
        System.out.println("O resultado da divisao de A e B é:" + c);
    }
}

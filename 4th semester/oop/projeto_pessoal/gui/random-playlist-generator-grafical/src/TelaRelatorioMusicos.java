    public void gerarRelatorio(BancoDeMusicos bmo) {
       setVisible(true);
        
       if(!bmo.getAll().isEmpty()) {
            DefaultTableModel modeloMusicas = (DefaultTableModel) tabMusicos.getModel();
            int i = 0;
            modeloMusicas.setRowCount(i);
            for(Musico musico : bmo.getAll()) {
                modeloMusicas.insertRow(i, new Object[]{
                    musico.getNome(),
                    musico.getFuncao(),
                    musico.getBanda().getNome()
                });
                i++;
            }
        }
    }

